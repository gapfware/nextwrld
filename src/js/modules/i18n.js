let currentLang = 'es'; // Default español

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
        el.setAttribute('placeholder', translations[lang][key]);
      } else if (el.hasAttribute('data-i18n-html')) {
        el.innerHTML = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });
  // Actualizar el documento lang
  document.documentElement.lang = lang;
  // Actualizar botones de idioma
  const esBtn = document.getElementById('lang-es');
  const enBtn = document.getElementById('lang-en');
  if (esBtn && enBtn) {
    esBtn.classList.toggle('tw-bg-purple-600', lang === 'es');
    esBtn.classList.toggle('tw-bg-gray-700', lang !== 'es');
    enBtn.classList.toggle('tw-bg-purple-600', lang === 'en');
    enBtn.classList.toggle('tw-bg-gray-700', lang !== 'en');
  }
  // Guardar en localStorage
  localStorage.setItem('lang', lang);
}

function initI18n() {
  // Cargar idioma guardado
  const savedLang = localStorage.getItem('lang') || 'es';
  setLanguage(savedLang);
}

export { setLanguage as changeLanguage, initI18n };
